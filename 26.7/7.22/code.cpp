class Solution {
public:
    int maxActiveSectionsAfterTrade(string s) {
        // total1：原始字符串中所有'1'的总数量
        // mx：交易后最多能新增的1的数量（两段连续0交换中间1区间得到）
        // pre0：上一段连续0的长度，初始极小值，保证首次计算不会乱更新
        // cnt：当前连续段的字符计数
        int total1 = 0, mx = 0, pre0 = INT_MIN, cnt = 0;

        for (int i = 0; i < s.size(); i++) {
            cnt++;
            // 判断条件：到达字符串末尾 或者 当前字符和下一个字符不同
            // 说明i是当前连续相同字符段的最后一位，结算本段
            if (i == s.size() - 1 || s[i] != s[i + 1]) {
                if (s[i] == '1') {
                    // 当前段是连续'1'，累加进总1数量
                    total1 += cnt;
                } else {
                    // 当前段是连续'0'
                    // 交换操作本质：选取【左0段 + 中间1段 + 右0段】，把中间1换到两侧0区间
                    // 收益 = 左侧0长度 + 右侧0长度，中间的1全部消失
                    // 遍历到当前0段时，尝试和上一段0组合，更新最大收益
                    mx = max(mx, pre0 + cnt);
                    // 更新上一段0长度为当前0段长度，供下一轮0段使用
                    pre0 = cnt;
                }
                // 清空计数，准备统计下一段连续字符
                cnt = 0;
            }
        }
        // 原始所有1 + 交换操作能新增的最大0数量（变成1）
        return total1 + mx;
    }
};